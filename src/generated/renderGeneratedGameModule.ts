import type {GameBlueprint} from "./GameBlueprint.js";
import type {GameBuildInfo} from "./GameBuildInfo.js";

// Plain CommonJS, not TypeScript: unlike "pokie create"/"pokie init" (which scaffold a TS project
// for a developer to hand-edit), "pokie build" output is meant to be immediately loadable with no
// separate compile step.
//
// "buildInfo" is optional purely for backward compatibility with existing callers of this exported
// function; GamePackageGenerator always supplies it, so real "pokie build" output always carries the
// header/build-info.json metadata below.
//
// Deliberately omits buildInfo.generatedAt: unlike build-info.json (whose whole point is a
// provenance timestamp), this file's content should only change when the blueprint/pokie
// version/source actually does, so re-running "pokie build" on an unchanged blueprint regenerates a
// byte-identical index.js — see build-info.json for "when".
export function renderGeneratedGameModule(blueprint: GameBlueprint, buildInfo?: GameBuildInfo): string {
    const {manifest} = blueprint;
    const header = buildInfo
        ? `// Game:            ${manifest.name} (id: "${manifest.id}", version ${manifest.version})
// Blueprint schema: v${buildInfo.schemaVersion}
// Generated by:     pokie ${buildInfo.pokieVersion}${buildInfo.source ? `\n// Source:           ${buildInfo.source}` : ""}
// Blueprint hash:    ${buildInfo.blueprintHash}
// Full provenance:   ./build-info.json`
        : `// Game: ${manifest.name} (id: "${manifest.id}", version ${manifest.version})`;

    // Absent winModel means today's default (line pays via VideoSlotConfig's own default win
    // calculator) — every existing blueprint keeps generating byte-identical output.
    const winModel = blueprint.winModel ?? {type: "lines" as const};
    const freeGames = blueprint.mechanics?.freeGames;
    const hasBetModes = (blueprint.betModes?.length ?? 0) > 0;

    const requireNames = ["CustomLinesDefinitions", "SymbolsSequence", "VideoSlotConfig", "VideoSlotSession", "VideoSlotSessionSerializer"];
    if (winModel.type === "ways" || winModel.type === "clusters") {
        requireNames.push("VideoSlotWinCalculator", "SelectedEvaluatorGroupWinAggregationPolicy");
        requireNames.push(winModel.type === "ways" ? "WaysWinCalculator" : "ClusterWinCalculator");
    }
    if (freeGames) {
        requireNames.push("VideoSlotWithFreeGamesConfig", "VideoSlotWithFreeGamesSession");
    }
    requireNames.sort();

    // Built once here (not inline in createConfig/createSession) since both the config-wrapping and
    // the session/win-calculator construction below need to agree on it.
    let winCalculatorDeclaration = "";
    if (winModel.type === "ways") {
        winCalculatorDeclaration = `    const winCalculator = new VideoSlotWinCalculator(config, undefined, undefined, undefined, undefined, new WaysWinCalculator(config), {
        aggregationPolicy: new SelectedEvaluatorGroupWinAggregationPolicy("ways"),
    });
`;
    } else if (winModel.type === "clusters") {
        winCalculatorDeclaration = `    const winCalculator = new VideoSlotWinCalculator(config, undefined, undefined, new ClusterWinCalculator(config, ${winModel.minimumClusterSize ?? 5}), undefined, undefined, {
        aggregationPolicy: new SelectedEvaluatorGroupWinAggregationPolicy("cluster"),
    });
`;
    }
    const winCalculatorArgs = winModel.type === "lines" ? "" : ", undefined, winCalculator";
    const sessionClassName = freeGames ? "VideoSlotWithFreeGamesSession" : "VideoSlotSession";
    // Only introduce an intermediate "config" local when a winCalculator actually needs to reference
    // it — otherwise createSession() stays the exact one-liner it's always been.
    const createSessionBody = winCalculatorDeclaration
        ? `        const config = createConfig();
${winCalculatorDeclaration}        return new ${sessionClassName}(config${winCalculatorArgs});
`
        : `        return new ${sessionClassName}(createConfig());
`;

    const freeGamesWrapCode = freeGames
        ? `
    const freeGamesConfig = new VideoSlotWithFreeGamesConfig(config);
    for (const [count, awarded] of Object.entries(blueprint.mechanics.freeGames.awardsByCount)) {
        freeGamesConfig.setFreeGamesForScatters(blueprint.mechanics.freeGames.scatterSymbol, Number(count), awarded);
    }
`
        : "";
    const configReturnExpression = freeGames ? "freeGamesConfig" : "config";

    const betModesExport = hasBetModes
        ? `
    getBetModes() {
        return blueprint.betModes;
    },`
        : "";

    return `// ============================================================================
// GENERATED FILE — do not hand-edit.
//
// Produced by "pokie build" from a GameBlueprint JSON config. Re-run "pokie build" (see this
// package's README.md) to regenerate this file after changing the blueprint; any manual edits here
// are overwritten on the next run.
//
${header}
// ============================================================================

// ---------------------------------------------------------------------------
// pokie runtime
// ---------------------------------------------------------------------------
const {
${requireNames.map((name) => `    ${name},`).join("\n")}
} = require("pokie");

// ---------------------------------------------------------------------------
// Blueprint data (embedded verbatim from the source GameBlueprint JSON config)
// ---------------------------------------------------------------------------
const blueprint = ${JSON.stringify(blueprint, null, 4)};

// ---------------------------------------------------------------------------
// VideoSlotConfig assembly
//
// Setter order matters here: setAvailableBets/setWildSymbols/setScatterSymbols must run before
// setAvailableSymbols, which rebuilds the paytable and (absent explicit reel strips) the default
// reel sequences from whatever wilds/scatters are set at that point. The paytable, paylines, and
// reel strips are set after setAvailableSymbols, since it would otherwise overwrite them. See
// VideoSlotConfig for the full contract.
// ---------------------------------------------------------------------------
function createConfig() {
    const config = new VideoSlotConfig();

    if (blueprint.availableBets) {
        config.setAvailableBets(blueprint.availableBets);
    }
    config.setReelsNumber(blueprint.reels);
    config.setReelsSymbolsNumber(blueprint.rows);
    config.setWildSymbols(blueprint.wilds || []);
    config.setScatterSymbols(blueprint.scatters || []);
    config.setAvailableSymbols(blueprint.symbols);

    const paytable = config.getPaytable();
    for (const symbolId of Object.keys(blueprint.paytable)) {
        const payouts = blueprint.paytable[symbolId];
        for (const times of Object.keys(payouts)) {
            paytable.setPayoutForSymbol(symbolId, Number(times), payouts[times]);
        }
    }

    if (blueprint.paylines) {
        const linesDefinitions = new CustomLinesDefinitions();
        blueprint.paylines.forEach((line, index) => linesDefinitions.setLineDefinition(String(index), line));
        config.setLinesDefinitions(linesDefinitions);
    }

    if (blueprint.reelStrips) {
        config.setSymbolsSequences(blueprint.reelStrips.map((strip) => new SymbolsSequence().fromArray(strip)));
    } else if (blueprint.symbolWeights) {
        const sequences = [];
        for (let i = 0; i < blueprint.reels; i++) {
            sequences.push(new SymbolsSequence().fromNumbersOfSymbols(blueprint.symbolWeights).shuffle());
        }
        config.setSymbolsSequences(sequences);
    }
${freeGamesWrapCode}
    return ${configReturnExpression};
}

// ---------------------------------------------------------------------------
// PokieGame contract (see docs/game-packages.md)
// ---------------------------------------------------------------------------
module.exports = {
    getManifest() {
        return blueprint.manifest;
    },
    createSession() {
${createSessionBody}    },
    getSessionSerializer() {
        return new VideoSlotSessionSerializer();
    },${betModesExport}
};
`;
}
