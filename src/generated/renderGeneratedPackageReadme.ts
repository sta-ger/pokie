import type {GameBlueprintManifest} from "./GameBlueprint.js";

// A short orientation doc dropped at the root of every "pokie build" package, so the output is
// understandable on its own — without reading GamePackageGenerator or this repository's docs.
export function renderGeneratedPackageReadme(manifest: GameBlueprintManifest): string {
    return `# ${manifest.name}

Generated by \`pokie build\` from a \`GameBlueprint\` JSON config. This package is playable as-is —
no build step — through the standard POKIE game package contract.

## Structure

- \`package.json\` — name/version/description from the blueprint's \`manifest\`, a \`pokie\` dependency,
  and \`start\`/\`server\`/\`client\` scripts.
- \`src/generated/index.js\` — the \`PokieGame\` implementation: a \`VideoSlotConfig\` assembled from the
  blueprint (reels, symbols, paytable, paylines, reel strips/weights), wrapped in a \`VideoSlotSession\`.
- \`src/generated/build-info.json\` — provenance for this generated output: schema version, the \`pokie\`
  version that generated it, a timestamp, and a hash of the source blueprint.

**Do not hand-edit anything under \`src/generated/\`** — re-run \`pokie build <config.json> --out .\` to
regenerate it after changing the blueprint. Manual edits are silently overwritten on the next run.

## Workflow

\`\`\`
npm install

npx pokie validate .
npx pokie sim . --rounds 10000 --seed demo --out sim.json
npx pokie report sim.json
npx pokie replay . --seed demo --round 1
npx pokie dev .
\`\`\`

See [\`pokie\`'s CLI docs](https://github.com/sta-ger/pokie/blob/master/docs/cli.md) for what each
command does and every available option.
`;
}
