import fs from "fs";
import path from "path";
import {buildGameBuildInfo, GENERATED_PACKAGE_FILES} from "./buildGameBuildInfo.js";
import type {GameBlueprint} from "./GameBlueprint.js";
import type {GameBuildInfo} from "./GameBuildInfo.js";
import type {GameBuildInfoReelStripGeneration} from "./GameBuildInfoReelStripGeneration.js";
import type {GamePackageGenerating} from "./GamePackageGenerating.js";
import type {GeneratedGamePackage} from "./GeneratedGamePackage.js";
import {materializeReelStrips} from "./materializeReelStrips.js";
import {renderGeneratedGameModule} from "./renderGeneratedGameModule.js";
import {renderGeneratedPackageReadme} from "./renderGeneratedPackageReadme.js";
import {resolveReelStripGeneration} from "./resolveReelStripGeneration.js";

const DEFAULT_DESCRIPTION = 'Generated by "pokie build" — a POKIE video slot game package.';
const BUILD_INFO_RELATIVE_PATH = "src/generated/build-info.json";

export class GamePackageGenerator implements GamePackageGenerating {
    private readonly pokieVersion: string;

    constructor(pokieVersion: string) {
        this.pokieVersion = pokieVersion;
    }

    // "sourcePath" is optional and purely informational: when given (BuildCommand passes the
    // <config.json> path it was invoked with), it's recorded in build-info.json for provenance.
    // Omitting it — e.g. when calling the generator directly with an in-memory blueprint — still
    // produces a fully valid package, just without a "source" field.
    //
    // "blueprint" here is the blueprint exactly as authored/validated — including its per-reel
    // reelStripGeneration array, if any. "reelStripGeneration" is that array's *already resolved*
    // result (see resolveReelStripGeneration.ts) — normally BuildCommand runs resolution itself first
    // (so it can report per-reel diagnostics its own way) and passes the result straight through. A
    // caller that omits it entirely — e.g. calling this generator directly with an in-memory blueprint
    // — gets resolution run for it right here, failing the same way BuildCommand would have.
    // blueprintHash/no-op-rebuild detection (see buildGameBuildInfo) are therefore keyed on the
    // *authored* config, not on whatever a generated reel happened to produce — two different authored
    // configs that happen to generate byte-identical strips still hash differently. The actual runtime
    // file (index.js) instead gets a *materialized* copy (materializeReelStrips) with reelStripGeneration
    // resolved into a plain reelStrips array — the runtime module never depends on the generation API.
    public generate(
        blueprint: GameBlueprint,
        cwd: string,
        outDir?: string,
        sourcePath?: string,
        reelStripGeneration?: GameBuildInfoReelStripGeneration,
    ): GeneratedGamePackage {
        const id = blueprint.manifest.id;
        if (outDir === undefined && (id.includes("/") || id.includes("\\") || id === "." || id === "..")) {
            throw new Error(
                `"manifest.id" ("${id}") is not a valid directory name. Use a plain name, e.g. "crazy-fruits", or pass --out <dir>.`,
            );
        }

        const resolvedReelStripGeneration =
            reelStripGeneration !== undefined ? reelStripGeneration : this.resolveReelStripGenerationOrThrow(blueprint);

        const projectRoot = outDir !== undefined ? path.resolve(cwd, outDir) : path.join(cwd, id);
        const projectRootExists = fs.existsSync(projectRoot);
        const previousBuildInfo = projectRootExists ? this.readPreviousBuildInfo(projectRoot) : undefined;
        if (projectRootExists) {
            this.assertSafeToRebuild(projectRoot);
        }

        const buildInfo = buildGameBuildInfo(
            blueprint,
            this.pokieVersion,
            sourcePath,
            new Date(),
            GENERATED_PACKAGE_FILES,
            previousBuildInfo,
            resolvedReelStripGeneration,
        );

        const packageJson = {
            name: id,
            version: blueprint.manifest.version,
            description: blueprint.manifest.description ?? DEFAULT_DESCRIPTION,
            ...(blueprint.manifest.author ? {author: blueprint.manifest.author} : {}),
            keywords: ["pokie", "video-slot", "generated"],
            private: true,
            scripts: {
                start: "pokie dev .",
                server: "pokie serve .",
                client: "pokie client .",
            },
            dependencies: {
                pokie: `^${this.pokieVersion}`,
            },
            pokie: {
                entry: "./src/generated/index.js",
            },
        };

        // materializeReelStrips can throw (see its own fail-fast checks) — it runs before any
        // filesystem write below (including mkdirSync) so a failure never creates or modifies
        // anything, whether it's this call's own resolution or a caller-supplied one.
        const materializedBlueprint = materializeReelStrips(blueprint, resolvedReelStripGeneration);

        fs.mkdirSync(path.join(projectRoot, "src", "generated"), {recursive: true});

        fs.writeFileSync(path.join(projectRoot, "package.json"), `${JSON.stringify(packageJson, null, 4)}\n`);
        fs.writeFileSync(path.join(projectRoot, "README.md"), renderGeneratedPackageReadme(blueprint.manifest));
        fs.writeFileSync(
            path.join(projectRoot, "src", "generated", "index.js"),
            renderGeneratedGameModule(materializedBlueprint, buildInfo),
        );
        fs.writeFileSync(
            path.join(projectRoot, "src", "generated", "build-info.json"),
            `${JSON.stringify(buildInfo, null, 4)}\n`,
        );

        return {
            projectRoot,
            manifest: blueprint.manifest,
            createdFiles: [...GENERATED_PACKAGE_FILES],
            buildInfo,
            unchanged: previousBuildInfo !== undefined && buildInfo.generatedAt === previousBuildInfo.generatedAt,
        };
    }

    // Runs resolveReelStripGeneration for a caller that didn't already do so (and pass its result in)
    // — mirrors what BuildCommand does with its own console.error reporting, but as a single thrown
    // error carrying every failed reel's diagnostics, since a direct caller has no per-reel reporting
    // of its own to hook into.
    private resolveReelStripGenerationOrThrow(blueprint: GameBlueprint): GameBuildInfoReelStripGeneration | undefined {
        const resolution = resolveReelStripGeneration(blueprint);
        if (resolution.success) {
            return resolution.reelStripGeneration;
        }

        const lines = resolution.reels
            .filter((reel) => !reel.success)
            .map((reel) => {
                const lastDiagnostic = reel.diagnostics[reel.diagnostics.length - 1];
                const violationLines = (lastDiagnostic?.violations ?? [])
                    .map((violation) => `      ${violation.constraintId}: ${violation.message}`)
                    .join("\n");
                return (
                    `  - reel ${reel.reelIndex} (seed ${reel.seed}): failed after ${reel.attemptsUsed} attempt(s)` +
                    (violationLines ? `\n${violationLines}` : "")
                );
            });

        throw new Error(`Blueprint "${blueprint.manifest.id}" could not generate its reel strips:\n${lines.join("\n")}`);
    }

    // Refuses to build into an existing, non-empty directory unless every fixed path we're about to
    // write (GENERATED_PACKAGE_FILES) is either absent or already known to have been produced by a
    // prior "pokie build" run. That's what lets re-running "pokie build" into the same --out directory
    // (e.g. after editing the blueprint) overwrite cleanly, while a directory that happens to already
    // have its own unrelated package.json/README.md/etc. is left untouched and reported as a conflict
    // instead of silently clobbered.
    //
    // "Known to have been produced by pokie build" is read from the existing directory's own
    // src/generated/build-info.json — itself pokie build output — rather than assumed: its "files"
    // field lists exactly what that run generated, so a newer generator recognizes an older one's
    // output even if the generated file set has changed since.
    private assertSafeToRebuild(projectRoot: string): void {
        if (!fs.statSync(projectRoot).isDirectory()) {
            throw new Error(
                `"${projectRoot}" already exists and is not a directory. Choose a different --out directory or remove it first.`,
            );
        }

        const knownFiles = this.readKnownGeneratedFiles(projectRoot);
        const conflicting = GENERATED_PACKAGE_FILES.filter(
            (relativePath) => !knownFiles.includes(relativePath) && fs.existsSync(path.join(projectRoot, ...relativePath.split("/"))),
        );

        if (conflicting.length > 0) {
            throw new Error(
                `"${projectRoot}" already exists and contains file(s) "pokie build" did not generate: ${conflicting.join(", ")}. ` +
                    `Refusing to overwrite them — choose a different --out directory, remove the conflicting file(s), or point ` +
                    `--out at a directory previously produced by "pokie build" (recognized via its own src/generated/build-info.json).`,
            );
        }
    }

    // Returns the file list an existing directory's own build-info.json vouches for, or [] if there's
    // no build-info.json there, it doesn't parse, or it wasn't written by "pokie build" — in which case
    // every fixed output path found on disk is treated as a conflict.
    private readKnownGeneratedFiles(projectRoot: string): string[] {
        const previous = this.readPreviousBuildInfo(projectRoot);
        if (previous === undefined) {
            return [];
        }
        return Array.isArray(previous.files) ? previous.files : GENERATED_PACKAGE_FILES;
    }

    // Returns the existing directory's own build-info.json, or undefined if there's none there, it
    // doesn't parse, or it wasn't written by "pokie build". Feeds both readKnownGeneratedFiles (the
    // rebuild-safety check) and buildGameBuildInfo (the generatedAt reuse-on-no-op-rebuild check).
    private readPreviousBuildInfo(projectRoot: string): GameBuildInfo | undefined {
        const buildInfoPath = path.join(projectRoot, ...BUILD_INFO_RELATIVE_PATH.split("/"));
        if (!fs.existsSync(buildInfoPath)) {
            return undefined;
        }

        try {
            const parsed = JSON.parse(fs.readFileSync(buildInfoPath, "utf-8"));
            if (!parsed || parsed.generatedBy !== "pokie build") {
                return undefined;
            }
            return parsed as GameBuildInfo;
        } catch {
            return undefined;
        }
    }
}
